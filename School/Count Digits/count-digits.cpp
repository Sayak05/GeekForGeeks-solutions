class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int count=0;
        int n=N;
        while(N){
            int rem=N%10;
            if(rem!=0 && n%rem==0)
                count++;
            N/=10;
        }
        return count;
    }
};

